package academy.devdojo.maratonajava.introducao;

public class Aula04Operadores {
    public static void main(String[] args) {
        // soma (+), subtração(-), divisão(/), multiplicação(*)

        int numero1 = 10;
        int numero2 = 20;
        double numero3 = 20;

        //soma
        int soma = numero1 + numero2;
        //subtração
        int subtracao = numero1 - numero2;
        //multiplicação
        int multiplicacao = numero1 * numero2;
        //divisão
        double divissao = numero1 / numero3;

        System.out.println("A soma é "+soma+"\n");
        System.out.println("A subtração é "+subtracao+"\n");
        System.out.println("A multiplicação é "+multiplicacao+"\n");
        System.out.println("A divisão é "+divissao+"\n");


    }
}
